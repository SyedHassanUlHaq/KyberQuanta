def bits_to_bytes(bit_array, l):
   
    
    B = [0] * l
    # print("B",len(B))
    for i in range(len(bit_array)):
        B[i // 8] = B[i // 8] + bit_array[i] * (2 ** ((i % 8))) 

    return B

